package com.heiheilianzai.app.model;

/**
 * 书城banner下方的gridview的bean
 */
public class EntranceItem {
    /**
     * 名称
     */
    String name;
    /**
     * 图片资源id
     */
    int resId;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getResId() {
        return resId;
    }

    public void setResId(int resId) {
        this.resId = resId;
    }
}
